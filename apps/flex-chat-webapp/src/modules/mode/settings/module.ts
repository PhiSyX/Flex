import type { Module } from "~/modules/interface";
import type { ChatStore } from "~/store/ChatStore";

import { ModeSettingsHandler } from "./handler";

// -------------- //
// Implémentation //
// -------------- //

export class ModeSettingsModule implements Module<ModeSettingsModule> {
	// ------ //
	// STATIC //
	// ------ //

	static NAME = "MODE_SETTINGS";

	static create(store: ChatStore): ModeSettingsModule {
		return new ModeSettingsModule(new ModeSettingsHandler(store));
	}

	// ----------- //
	// Constructor //
	// ----------- //
	constructor(private handler: ModeSettingsHandler) {}

	input() {}

	send() {}

	listen() {
		this.handler.listen();
	}
}
