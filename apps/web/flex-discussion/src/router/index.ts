import type { RouterContract } from "@phisyx/flex-architecture/router";

import { Option } from "@phisyx/flex-safety/option";
import { useRoute, useRouter } from "vue-router";

// -------------- //
// Implémentation // -> Interface
// -------------- //

export class VueRouter implements RouterContract {
	public inner = useRouter();
	public current = useRoute();

	back() {
		this.inner.back();
	}

	forward() {
		this.inner.forward();
	}

	goto(name: string, opt: object = {}) {
		this.inner.push({ name, ...opt });
	}

	param(name: string): Option<string> {
		return Option.from(this.current.params[name]).as<string>();
	}
}
