import { computed } from "vue";

// ----------- //
// Local State //
// ----------- //

export const computeInputPlaceholder = ({
	disableInput,
	name,
}: {
	disableInput: boolean;
	name: string;
}) =>
	computed(() => {
		return disableInput
			? `La chambre « ${name} » est en mode lecture uniquement.`
			: "Commencez à taper / pour obtenir la liste des commandes disponibles...";
	});
