export interface Emits {
	(evtName: "open-private", nickname: string): void;
}

// -------- //
// Handlers //
// -------- //

export function openPrivate(emit: Emits) {
	function openPrivateHandler(nickname: string) {
		emit("open-private", nickname);
	}
	return openPrivateHandler;
}
