import { Browser, Page } from "@playwright/test";

export function createNBrowserPages(n: number) {
	return async (browser: Browser) => {
		const pages: Array<Promise<Page>> = Array.from({ length: n }, () =>
			browser.newContext().then((ctx) => ctx.newPage()),
		);
		return pages;
	};
}

export function generateRandomWord() {
	return `x${(Math.random() + 1).toString(36).slice(2)}x`;
}

export function generateRandomChannel() {
	return `#test-x${(Math.random() + 1).toString(36).slice(2)}x`;
}
