export class PrivateNick {
	// ----------- //
	// Constructor //
	// ----------- //
	constructor(private origin: Origin) {}

	// --------------- //
	// Getter | Setter //
	// --------------- //

	get nickname() {
		return this.origin.nickname;
	}

	get ident() {
		return this.origin.ident;
	}

	get hostname() {
		return this.origin.host.vhost || this.origin.host.cloaked;
	}
}
