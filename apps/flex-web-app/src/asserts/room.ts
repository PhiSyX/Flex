/**
 * Certifie que l'argument donné est une chambre de type ChannelRoom aka
 * "channel".
 */
export function assertChannelRoom(room: {
	type: string;
}): asserts room is ChannelRoom {
	if (room.type !== "channel") {
		throw new Error(
			`« ${room} » n'est pas une chambre de type « channel »`,
		);
	}
}
