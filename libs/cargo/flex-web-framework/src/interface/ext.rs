// --------- //
// Interface //
// --------- //

pub trait ExtensionInterface: Sized + Clone + Send + Sync + 'static
{
	type Payload;

	fn new(payload: Self::Payload) -> Self;
}

#[allow(async_fn_in_trait)]
pub trait AsyncExtensionInterface: Sized + Clone + Send + Sync + 'static
{
	type Payload;

	async fn new(payload: Self::Payload) -> Self;
}

// -------------- //
// Implémentation //
// -------------- //

impl ExtensionInterface for ()
{
	type Payload = Self;

	fn new(_: Self::Payload) -> Self {}
}

impl AsyncExtensionInterface for ()
{
	type Payload = Self;

	async fn new(_: Self::Payload) -> Self {}
}
