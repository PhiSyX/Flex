// --------- //

#[derive(Debug)]
#[derive(serde::Serialize, serde::Deserialize)]
pub struct Origin<C>
where
	C: super::ClientInterface,
{
	pub id: C::ClientID,
	#[serde(flatten)]
	pub user: C::User,
}

// -------------- //
// Implémentation // -> Interface
// -------------- //

impl<C> From<&C> for Origin<C>
where
	C: super::ClientInterface,
{
	fn from(client: &C) -> Self
	{
		Self {
			id: client.cid().clone(),
			user: client.user().clone(),
		}
	}
}
