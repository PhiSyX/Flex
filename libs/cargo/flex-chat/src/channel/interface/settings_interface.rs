use std::collections::HashMap;

use crate::channel::SettingsFlagInterface;
use crate::mode::ApplyMode;

// --------- //
// Interface //
// --------- //

pub trait ChannelSettingsInterface
{
	type SettingsFlag: SettingsFlagInterface;

	/// Crée une nouvelle structure d'un salon avec des drapeaux.
	fn with_creation_flags(
		self,
		flags: impl IntoIterator<Item = ApplyMode<Self::SettingsFlag>>,
	) -> Self;

	/// Définit la clé du salon.
	fn set_key(&mut self, updated_by: &str, key: impl ToString);

	/// Paramètres du salon.
	fn settings(&self) -> HashMap<char, ApplyMode<Self::SettingsFlag>>;
}
