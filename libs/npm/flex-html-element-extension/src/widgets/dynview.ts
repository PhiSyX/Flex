import type { Signal } from "@phisyx/flex-signal";
import type { HTMLElementExtension as Ext } from "../extension";

export function dynview<T>(
	signalKey: Signal<T>,
	shallowView: (key: T) => Ext,
): Ext {
	return shallowView(signalKey.valueOf());
}
