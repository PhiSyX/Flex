import { IMAGE_CACHE } from "./cache";

export function load_image(
	src: string,
	fallback: string,
	image: HTMLImageElement | null,
) {
	let loaded = image?.getAttribute("src") !== fallback;

	let img = IMAGE_CACHE.get(src);
	if (img) {
		img.loaded = loaded;
	}
}

export function error_fallback(src: string, fallback: string) {
	let img = IMAGE_CACHE.get(src);
	if (img) {
		img.loaded = false;
		img.source = fallback;
	}
}
