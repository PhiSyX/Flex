export const DEFAULT_LABEL_NO = "Non";
export const DEFAULT_LABEL_YES = "Oui";

export function input_switch_state(input: unknown, disabled: boolean) {
	let state = input ? "Activé" : "Désactivé";
	if (disabled) {
		return `${state} (verrouillé)`;
	}
	return state;
}
