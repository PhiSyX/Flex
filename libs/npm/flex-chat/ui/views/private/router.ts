import type { RouterContract } from "@phisyx/flex-architecture/router";

// -------------- //
// Implémentation //
// -------------- //

export class PrivateRouter {
	static PARAM_ID = "id";

	constructor(private router_acl: RouterContract) {}

	param_id(): UserID {
		return this.router_acl
			.param(PrivateRouter.PARAM_ID)
			.as<UserID>()
			.unwrap_unchecked();
	}
}
