import type { ChatStoreInterface } from "@phisyx/flex-chat/store/chat";

import { ServerCustomRoom } from "@phisyx/flex-chat/custom_room/server";

// -------------- //
// Implémentation //
// -------------- //

export class SettingsChatManager {
	constructor(private store: ChatStoreInterface) {}

	is_active_room_server() {
		return (
			this.store.is_connected() &&
			this.store.room_manager().active().id() === ServerCustomRoom.ID
		);
	}
}
