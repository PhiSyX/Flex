import type { ChatStoreInterface } from "@phisyx/flex-chat/store/chat";

import { PrivateListChatManager } from "./datamanager/chat_data_manager";
import { PrivateListInteractor } from "./interactor";
import { PrivateListPresenter } from "./presenter";
import { PrivateListView } from "./view";

// -------------- //
// Implémentation //
// -------------- //

export class PrivateListWireframe {
	static create(chat_store: ChatStoreInterface) {
		let interactor = new PrivateListInteractor(
			new PrivateListPresenter(new PrivateListView()),
			[new PrivateListChatManager(chat_store)],
		);
		return interactor.presenter.view;
	}

	declare _: number;
}
