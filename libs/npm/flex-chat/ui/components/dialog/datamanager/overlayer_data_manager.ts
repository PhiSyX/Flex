import type { OverlayerStore } from "@phisyx/flex-chat/store/overlayer";

// -------------- //
// Implémentation //
// -------------- //

export class DialogOverlayerManager {
	constructor(public store: OverlayerStore) {}

	destroy_dialog(id: string) {
		return this.store.destroy(id);
	}

	has_data_dialog(id: string) {
		return this.store.get(id).unwrap().data != null;
	}

	get_data_dialog(id: string) {
		return this.store.get(id).unwrap().data || {};
	}

	is_dialog_exists(id: string) {
		return this.store.has(id);
	}

	update_dialog(id: string) {
		return this.store.update(id);
	}
}
