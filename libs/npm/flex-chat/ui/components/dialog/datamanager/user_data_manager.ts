import type { UserStore } from "@phisyx/flex-chat/store";
import type { UserSession } from "@phisyx/flex-chat/user/session";

// -------------- //
// Implémentation //
// -------------- //

export class DialogUserManager {
	constructor(private store: UserStore) {}

	patch_account(user_id: UserSession["id"], form_data: FormData) {
		this.store.patch(user_id, form_data);
	}

	unset_session_user() {
		this.store.unset_session();
	}
}
